function S(art){
  const arr = art.replace(/^\n/, '').replace(/\s+$/, '').split('\n').map(s => s.replace(/^\s+/, ''));
  let w = 0; for(const r of arr) w = Math.max(w, r.length);
  return arr.map(r => r.padEnd(w, '.'));
}

