/* ---------------- renderer ---------------- */
function drawSprite(canvas, name, scale){
  const lines = SPRITES[name];
  if(!lines){ return; }
  const H = lines.length, W = lines[0].length;
  const dpr = Math.max(1, window.devicePixelRatio || 1);
  const px = scale * dpr;
  canvas.width = W * px;
  canvas.height = H * px;
  const ctx = canvas.getContext('2d');
  ctx.imageSmoothingEnabled = false;
  ctx.clearRect(0, 0, canvas.width, canvas.height);
  for(let y = 0; y < H; y++){
    const row = lines[y];
    for(let x = 0; x < W; x++){
      const c = PAL[row[x]];
      if(c){ ctx.fillStyle = c; ctx.fillRect(x * px, y * px, px, px); }
    }
  }
}

function paintAllSprites(){
  document.querySelectorAll('canvas[data-spr]').forEach(cv => {
    const name = cv.getAttribute('data-spr');
    const scale = parseInt(cv.getAttribute('data-scale') || '4', 10);
    drawSprite(cv, name, scale);
  });
}

/* ---------------- stage scaler (fixed 16:9, desktop only) ---------------- */
function fitStage(stageSel, baseW, baseH){
  const stage = document.querySelector(stageSel);
  if(!stage) return;
  const apply = () => {
    if(window.matchMedia('(max-width:860px)').matches){ stage.style.transform = 'none'; return; }
    const s = Math.min(window.innerWidth / baseW, window.innerHeight / baseH);
    stage.style.transform = `translate(-50%, -50%) scale(${s})`;
  };
  apply();
  window.addEventListener('resize', apply);
}

