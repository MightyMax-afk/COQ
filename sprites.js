const HERO_ORDER = ['boots', 'armor', 'weapon', 'shield', 'helmet'];
// eq: { boots, armor, weapon, shield, helmet } -> layer key or null/undefined
function drawLayeredHero(canvas, eq, scale, baseKey){
  const base = (baseKey && SPRITES[baseKey]) || SPRITES.pd_base;
  const H = base.length, W = base[0].length;
  const dpr = Math.max(1, window.devicePixelRatio || 1);
  const px = scale * dpr;
  canvas.width = W * px;
  canvas.height = H * px;
  const ctx = canvas.getContext('2d');
  ctx.imageSmoothingEnabled = false;
  ctx.clearRect(0, 0, canvas.width, canvas.height);
  const stack = [base];
  for(const slot of HERO_ORDER){
    const key = eq && eq[slot];
    if(key && SPRITES[key]) stack.push(SPRITES[key]);
  }
  for(const lines of stack){
    for(let y = 0; y < lines.length; y++){
      const row = lines[y];
      for(let x = 0; x < row.length; x++){
        const c = PAL[row[x]];
        if(c){ ctx.fillStyle = c; ctx.fillRect(x * px, y * px, px, px); }
      }
    }
  }
}

